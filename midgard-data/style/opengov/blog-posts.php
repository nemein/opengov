                                    <p class="pubdate">2009-11-27
                                    </p>
                                    Julkishallinnon datavarantojen avaaminen on voimistuva trendi 
                                    maailmalla. Valtioiden ja yksittäisten kaupunkien virallisia 
                                    datakatalogeja on julkaistu viimeisen vuoden aikana lukuisia, 
                                    esimerkiksi USA:n http://www.data.gov/.
                                    Opengov.fi on suomalainen pääasiassa ohjelmoijille ja palveluideoijille 
                                    suunnattu sivusto, johon kerätään yhteisöllisesti kotimaisia 
                                    datalähteitä ja ideoita niiden hyödyntämiseksi. Sivusto saatetaan 
                                    toimimaan tiiviissä yhteistyössä suomi.fi portaaliin koottavan 
                                    virallisen datakatalogin (www.suomi.fi/datakatalogi) kanssa. 
