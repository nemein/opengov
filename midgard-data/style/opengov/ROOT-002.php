                            <h1>Tervetuloa
                            </h1>
                            <p>Internet on nykymaailman avoin tila ja sen avulla valtioilla on nyt 
                                mahdollisuus paremmin ymmärtää kansalaistensa tarpeita sekä kansalaisten
                                vaikuttaa täydemmin hallintoonsa. Tiedon arvo kasvaa sitä jaettaessa ja
                                vähenee jos sitä pidetään piilossa. Avoin data edustaa suurempaa 
                                kansalaisten kanssakäymistä, kehittyneempää julkista sektoria sekä 
                                tehokkaampaa yhteisten resurssien hyödyntämistä.
                            </p>
                            <hr>
                            <h2>Uutisia
                            </h2>
                            <div class="blogposts">
                                <div class="post">
                                    <h3>
                                        <a href="http://opengov.fi/blog/2009/kehittajien-oma-datakatalogi/">Kehittäjien
                                            oma datakatalogi
                                        </a>
                                    </h3>
                                    <p class="pubdate">2009-11-27
                                    </p>
                                    Julkishallinnon datavarantojen avaaminen on voimistuva trendi 
                                    maailmalla. Valtioiden ja yksittäisten kaupunkien virallisia 
                                    datakatalogeja on julkaistu viimeisen vuoden aikana lukuisia, 
                                    esimerkiksi USA:n http://www.data.gov/.
                                    Opengov.fi on suomalainen pääasiassa ohjelmoijille ja palveluideoijille 
                                    suunnattu sivusto, johon kerätään yhteisöllisesti kotimaisia 
                                    datalähteitä ja ideoita niiden hyödyntämiseksi. Sivusto saatetaan 
                                    toimimaan tiiviissä yhteistyössä suomi.fi portaaliin koottavan 
                                    virallisen datakatalogin (www.suomi.fi/datakatalogi) kanssa. 
                                    <p class="postmeta">
                                        <a href="http://opengov.fi/blog/2009/kehittajien-oma-datakatalogi/">Keskustele
                                            <span class="outside">Kehittäjien oma datakatalogi 
                                            </span>(4 kommentit)
                                        </a>
                                    </p>
